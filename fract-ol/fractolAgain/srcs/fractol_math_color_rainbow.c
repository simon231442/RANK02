#include "fractol.h"

int	fractol_math_color_rainbow(int iter, int max_iter)
{
	double	t;
	int		r;
	int		g;
	int		b;

	if (iter == max_iter)
		return (0x000000);
	t = (double)iter / 32.0;
	r = (int)(sin(0.3 * t + 0) * 127 + 128);
	g = (int)(sin(0.3 * t + 2) * 127 + 128);
	b = (int)(sin(0.3 * t + 4) * 127 + 128);
	return (r << 16 | g << 8 | b);
}
